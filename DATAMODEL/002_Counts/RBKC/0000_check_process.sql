Check supply
Deal with suspensions and ensure consistent across all time periods
Check dual restrictions and ensure that hours of control table set up correctly

For bays
1. Check for obvious errors - high stress v capacity
2. Check for obvious misses - all other times periods with demand
3. Dual restrictions - make sure that only demand in one bay for each time period
4. Remove demand from unusual situations - bus stops/stands/cycle hangars/cycle hire

For lines
1. Check DYLs/DRLs for obvious errors (should be on neighbouring restrictions)
2. Check other non-parking areas - crossings, zig-zags